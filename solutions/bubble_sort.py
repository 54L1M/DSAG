"""Reference solution — bubble sort."""

from __future__ import annotations


def bubble_sort(arr: list[int]) -> None:
    n = len(arr)
    for i in range(n):
        swapped = False
        for j in range(n - 1 - i):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        if not swapped:
            break
